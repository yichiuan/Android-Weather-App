public class Atmosphere {
    private byte humidity;
    private float pressure;
    private byte rising;
    private float visibility;

    public byte getHumidity() {
        return humidity;
    }

    public float getPressure() {
        return pressure;
    }

    public byte getRising() {
        return rising;
    }

    public float getVisibility() {
        return visibility;
    }
}
